# 2. Buat contoh kode program untuk membedakan type data di python

# - tipe data list
nama_makanan = ["bakso", "kwetiau goreng", "donat", "martabak"]
print("list makanan : ", nama_makanan)
nilai_tugas = [80, 79, 85, 90]
print("nilai tugas data pemograman : ", nilai_tugas)

# - tipe data tuple
lagu_seventeen =('circles', 'hot', 'fear', 'clap',)
print("lagu seventeen : ", lagu_seventeen)

# - tipe data set
grup_kpop ={'BTS', 'straykids', 'treasure', 'seventeen'}
print("kpop boy grup : ", grup_kpop)

# - tipe data dictionary
data_dict ={
    'key':'value',
    'makanan':'bakso',
    'nilai':'90',
    'list':lagu_seventeen,
}
print(data_dict['key'])
print(data_dict['makanan'])
